# Arithmetic operators in action

a = 10
b = 3

# addition
add = a + b

# subtraction
diff = a - b

# multiplication
prod = a * b

# division
quotient = a / b

# Remainder
rem = a % b

# Exponent
exp = a**b

print("The sum of {} and {} : {}".format(a, b, add))
print("The difference of {} and {} : {}".format(a, b, diff))
print("Product of {} and {} : {}".format(a, b, prod))
print("The Quotient of {} and {} : {}".format(a, b, quotient))
print("The remainder of {} and {} : {}".format(a, b, rem))
print("{} power {} is : {}".format(a, b, exp))
